{
    ans=parseInt(a)+parseInt(b);
    return ans;
}
exports.sub=function(a,b)
{
    ans=parseInt(a)-parseInt(b);
    return ans;
}
exports.mul=function(a,b)
{
    ans=parseInt(a)*parseInt(b);
    return ans;
}
exports.div=function(a,b)
{
    ans=parseInt(a)/parseInt(b);
    return ans;
}